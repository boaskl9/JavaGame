package com.game.systems.item;

/**
 * Enumeration of item types.
 * Extensible for future item categories.
 */
public enum ItemType {
    CONSUMABLE,     // Potions, food, etc.
    WEAPON,         // Swords, axes, bows, etc.
    ARMOR,          // Chest, helmet, boots, etc.
    TOOL,           // Pickaxe, shovel, etc.
    MATERIAL,       // Crafting materials, resources
    QUEST,          // Quest-specific items
    RESOURCE,       //
    MISC            // Other items
}
